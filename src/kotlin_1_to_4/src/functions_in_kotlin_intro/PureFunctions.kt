/**
 * What are pure functions?
 *
 * It's a concept in functional programming ->
 *
 * function should always return same output for same input
 * function should not create any side effects
 *
 *
 * how do we know if it's pure?
 *
 * if we replace the function with result of the expression and the resulting value of program is still the same
 * then it is a pure function.
